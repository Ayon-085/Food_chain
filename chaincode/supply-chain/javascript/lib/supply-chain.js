    async loginUser(ctx, email, password) {
        console.info('============= START : Login User ===========');
        
        // query user based on email and password
        //if info is correct return successful
        let queryString = {};
        queryString.selector = {};
        queryString.selector.docType = 'user';
        queryString.selector.email = email;
        queryString.selector.password = password;

        //run query
        let queryResults = await this.getQueryResultForQueryString(ctx, JSON.stringify(queryString), thisClass);
        if (!queryResults || queryResults.length === 0) {
            throw new Error('Wrong Email or Password');
        }
        let users = JSON.parse(queryResults.toString());

        if(users.length === 0){
            throw new Error('Wrong Email or Password');
        }

        return users[0].key;

        console.info('============= END : Login User ===========');
    }

    async getQueryResultForQueryString(ctx, queryString) {

        console.info('- getQueryResultForQueryString queryString:\n' + queryString);
        let resultsIterator = await ctx.stub.getQueryResult(queryString);
    
        let results = await getAllResults(resultsIterator, false);
    
        return Buffer.from(JSON.stringify(results));
    }

    async getAllResults(iterator, isHistory) {
        let allResults = [];
        while (true) {
          let res = await iterator.next();
    
          if (res.value && res.value.value.toString()) {
            let jsonRes = {};
            console.log(res.value.value.toString('utf8'));
    
            if (isHistory && isHistory === true) {
              jsonRes.TxId = res.value.tx_id;
              jsonRes.Timestamp = res.value.timestamp;
              jsonRes.IsDelete = res.value.is_delete.toString();
              try {
                jsonRes.Value = JSON.parse(res.value.value.toString('utf8'));
              } catch (err) {
                console.log(err);
                jsonRes.Value = res.value.value.toString('utf8');
              }
            } else {
              jsonRes.Key = res.value.key;
              try {
                jsonRes.Record = JSON.parse(res.value.value.toString('utf8'));
              } catch (err) {
                console.log(err);
                jsonRes.Record = res.value.value.toString('utf8');
              }
            }
            allResults.push(jsonRes);
          }
          if (res.done) {
            console.log('end of data');
            await iterator.close();
            console.info(allResults);
            return allResults;
          }
        }
    }
